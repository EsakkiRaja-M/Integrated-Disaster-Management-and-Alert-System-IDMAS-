    public void Selectres() {

        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("SELECT * FROM events");
            ResultSet rs = ps.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();

            DefaultTableModel tbl = new DefaultTableModel();

            int col = rsmd.getColumnCount();
            String[] colname = new String[col];
            for (int i = 0; i < col; i++) {
                colname[i] = rsmd.getColumnName(i + 1);
            }

            tbl.setColumnIdentifiers(colname);

            while (rs.next()) {
                String titlee = rs.getString(1);
                String descc = rs.getString(2);
                String orgg = rs.getString(3);
                String venuee = rs.getString(4);
                String datee = rs.getString(5);
                String timee = rs.getString(6);

                String[] tdata = {titlee, descc, orgg, venuee, datee, timee};
                tbl.addRow(tdata);
            }

            // Set the new model to the JTable
            jTable1.setModel(tbl);

            con.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }

