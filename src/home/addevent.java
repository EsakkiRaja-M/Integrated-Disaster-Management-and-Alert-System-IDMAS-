    private void delbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delbtnMouseClicked
        if (title.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "SELECT EVENT TITLE TO DELETE");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                String titlee = title.getText();
                String query = "DELETE FROM events WHERE TITLE=?";
                try (PreparedStatement pstmt = con.prepareStatement(query)) {
                    pstmt.setString(1, titlee);
                    pstmt.executeUpdate();
                }
                Selectres();
                clear();
                JOptionPane.showMessageDialog(null, "EVENT DETAILS DELETED");
                clear();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error :" + e.getMessage());
            } finally {
                try {
                    Connection con = ConnectionProvider.getCon();
                    if (con != null) {
                        con.close();
                    }
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_delbtnMouseClicked

