    private void addbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addbtnMouseClicked

        String titlee = title.getText();
        String descc = desc.getText();
        String orgg = org.getText();
        String venuee = venue.getText();
        String datee = date.getText();
        String timee = time.getText();
        if (title.getText().isEmpty() || desc.getText().isEmpty() || org.getText().isEmpty() || venue.getText().isEmpty() || date.getText().isEmpty() || time.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "PLEASE FILL DEATILS");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into events values(?,?,?,?,?,?)");
                ps.setString(1, titlee);
                ps.setString(2, descc);
                ps.setString(3, orgg);
                ps.setString(4, venuee);
                ps.setString(5, datee);
                ps.setString(6, timee);

                int rowsAffected = ps.executeUpdate();

                if (rowsAffected > 0) {
                   if (check.isSelected()) {
        // Checkbox is selected, redirect to the email send page
        String eventname =title.getText();
        String eventdesc = desc.getText();
        String eventorg = org.getText();
        String eventvenue = venue.getText();
        String eventdate = date.getText();
        String eventtime = time.getText();
Selectres();
clear();
        email_loc emailPage = new email_loc(eventname,eventorg , eventdesc,eventvenue, eventdate, eventtime);
        emailPage.setVisible(true);
        
        this.dispose(); // Close the current page if needed
    }
                    else{
                    JOptionPane.showMessageDialog(null, "EVENT DEATILS ADDED");
                    Selectres();
                    clear();}
                } else {
                    JOptionPane.showMessageDialog(null, "INSERTION FAILED");
                    clear();
                }

                con.close();

            } catch (HeadlessException | SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_addbtnMouseClicked

