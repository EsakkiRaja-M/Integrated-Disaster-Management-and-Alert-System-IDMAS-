    private void addbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addbtnMouseClicked
        String namee=name.getText();
        String idd=id.getText();
         String qunn=qun.getText();
          String cityy=city.getText();
           String statee=state.getText();
       if (id.getText().isEmpty() ||name.getText().isEmpty() || qun.getText().isEmpty() || city.getText().isEmpty()|| state.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "PLEASE FILL DEATILS");
        } else {
            try {
                Connection con=ConnectionProvider.getCon();
                PreparedStatement ps=con.prepareStatement("insert into resources values(?,?,?,?,?)");
                ps.setInt(1, Integer.valueOf(id.getText()));
            ps.setString(2, namee);
            ps.setString(3,qunn);
            ps.setString(4,cityy);
            ps.setString(5,statee);

                int rowsAffected =  ps.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "RESOURCES DETAILS ADDED ");
                    Selectres();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(null, "INSERTION FAILED");
                    clear();
                }

                con.close();

            }
            catch (HeadlessException | SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            }}
    }//GEN-LAST:event_addbtnMouseClicked

