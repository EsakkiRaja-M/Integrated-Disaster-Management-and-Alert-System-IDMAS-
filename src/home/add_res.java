        public void Selectres(){
     
    try {
       Connection con=ConnectionProvider.getCon();
       PreparedStatement ps = con.prepareStatement("SELECT * FROM resources");
      ResultSet  rs = ps.executeQuery();
        ResultSetMetaData rsmd = rs.getMetaData();

        DefaultTableModel tbl = new DefaultTableModel();

        int col = rsmd.getColumnCount();
        String[] colname = new String[col];
        for (int i = 0; i < col; i++) {
            colname[i] = rsmd.getColumnName(i + 1);
        }

        tbl.setColumnIdentifiers(colname);

        while (rs.next()) {
            String idd = String.valueOf(rs.getInt(1));
            String namee = rs.getString(2);
            String qunn = rs.getString(3);
            String cityy = rs.getString(4);
            String statee = rs.getString(5);

            String[] tdata = {idd,namee, qunn, cityy, statee};
            tbl.addRow(tdata);
        }

        // Set the new model to the JTable
        jTable1.setModel(tbl);

        con.close();
    } catch (Exception e) {
        System.out.println(e.getMessage());
    }


    }

